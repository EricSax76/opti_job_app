import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:opti_job_app/modules/evaluations/models/evaluation.dart';
import 'package:opti_job_app/modules/evaluations/models/scorecard_template.dart';
import 'package:opti_job_app/modules/evaluations/repositories/evaluation_repository.dart';

part 'evaluation_form_state.dart';

class EvaluationFormCubit extends Cubit<EvaluationFormState> {
  final EvaluationRepository _repository;

  EvaluationFormCubit({
    required EvaluationRepository repository,
  })  : _repository = repository,
        super(const EvaluationFormState());

  void init(ScorecardTemplate template, {Evaluation? existingEvaluation}) {
    if (existingEvaluation != null) {
      emit(state.copyWith(
        template: template,
        criteriaRatings: {
          for (var c in existingEvaluation.criteria) c.id: c.rating,
        },
        criteriaNotes: {
          for (var c in existingEvaluation.criteria) c.id: c.notes,
        },
        comments: existingEvaluation.comments,
        recommendation: existingEvaluation.recommendation,
        overallScore: existingEvaluation.overallScore,
      ));
    } else {
      emit(state.copyWith(
        template: template,
        criteriaRatings: {for (var c in template.criteria) c.id: 3},
        criteriaNotes: {for (var c in template.criteria) c.id: ''},
        recommendation: Recommendation.neutral,
        overallScore: 3.0,
      ));
    }
  }

  void updateRating(String criteriaId, int rating) {
    final updatedRatings = Map<String, int>.from(state.criteriaRatings)..[criteriaId] = rating;
    final overallScore = _calculateOverallScore(updatedRatings, state.template!.criteria);
    emit(state.copyWith(
      criteriaRatings: updatedRatings,
      overallScore: overallScore,
    ));
  }

  void updateNotes(String criteriaId, String notes) {
    final updatedNotes = Map<String, String>.from(state.criteriaNotes)..[criteriaId] = notes;
    emit(state.copyWith(criteriaNotes: updatedNotes));
  }

  void updateRecommendation(Recommendation recommendation) {
    emit(state.copyWith(recommendation: recommendation));
  }

  void updateComments(String comments) {
    emit(state.copyWith(comments: comments));
  }

  double _calculateOverallScore(Map<String, int> ratings, List<ScorecardCriteria> criteria) {
    double totalWeight = 0;
    double weightedSum = 0;
    for (var c in criteria) {
      final rating = ratings[c.id] ?? 3;
      weightedSum += rating * c.weight;
      totalWeight += c.weight;
    }
    return totalWeight > 0 ? weightedSum / totalWeight : 0;
  }

  Future<void> submitEvaluation({
    required String applicationId,
    required String jobOfferId,
    required String companyId,
    required String evaluatorUid,
    required String evaluatorName,
  }) async {
    if (state.template == null) return;

    emit(state.copyWith(status: EvaluationFormStatus.submitting));

    try {
      final evaluation = Evaluation(
        id: '', // Will be generated by Firestore or Cloud Function
        applicationId: applicationId,
        jobOfferId: jobOfferId,
        companyId: companyId,
        evaluatorUid: evaluatorUid,
        evaluatorName: evaluatorName,
        criteria: state.template!.criteria.map((c) {
          return EvaluationCriteria(
            id: c.id,
            name: c.name,
            rating: state.criteriaRatings[c.id] ?? 3,
            weight: c.weight,
            notes: state.criteriaNotes[c.id] ?? '',
          );
        }).toList(),
        overallScore: state.overallScore,
        recommendation: state.recommendation,
        comments: state.comments,
        createdAt: DateTime.now(),
      );

      await _repository.submitEvaluation(evaluation);
      emit(state.copyWith(status: EvaluationFormStatus.success));
    } catch (e) {
      emit(state.copyWith(status: EvaluationFormStatus.failure));
    }
  }
}
